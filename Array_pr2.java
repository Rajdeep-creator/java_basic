package array;

public class Array_pr2 {

	public static void main(String[] args) {
	int []element= {23,33,45,67,89};
	
	for(int i = 0;i<element.length;i++)
	{
		System.out.print(element[i]+" ");
		
	}
	System.out.println(" ");
	
	for(int m:element)
	{	
		System.out.print(m);
		System.out.println(" ");
		
	}


	}

}
